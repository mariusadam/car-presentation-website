<h2>BMW X4 xDrive35d 313CP </h2> <br />
<div id="main">
 <P><img src="images/n22_pret.jpg"></P>
 <p> <img src="images/n22_date.jpg" ></p>
 <p>&nbsp&nbsp&nbsp&nbsp Cu sapte ani in urma, BMW lansa un model cu un design 
 extravagant numit X6. Ei l-au descris ca fiind un "SAC", acronim pentru "Sports 
 Activity Coupe". Modelul respectiv avea un interior confortabil si o tinuta de 
 drum excelenta, cu toate ca locurile din spate erau doar doua si nu ofereau foarte 
 mult spatiu pentru cap, iar capacitatile sale la iesirea de pe sosea erau limitate 
 drastic de anvelopele din dotare si de configuratia barelor de protectie si a garzii la sol.
 <br />&nbsp&nbsp&nbsp&nbsp
 BMW X4 te primeste la bord cu doua scaune frontale bine profilate si imbracate in
 piele de calitate. Soferului i se pune in fata un volan gros, cu design sportiv, 
 echipat cu padele si multe butoane, reglabil electric pe inaltime si profunzime. 
 Toate materialele de la bord sunt atent finisate si aranjate. Comenzile sunt si 
 ele amplasate ergonomic si un sofer care a mai urcat intr-un BMW lansat in ultimii 
 ani va gasi cu siguranta tot ce are nevoie.
  <br />&nbsp&nbsp&nbsp&nbsp X4 are un avantaj in fata lui X6, ?n afara de faptul 
  ca este mai bine potrivit conceptului de SAC decat primul model care a fost construit 
  astfel. Iar avantajul acela vine la nivel de dimensiuni, care sunt mult mai potrivite
  pentru mediul urban si pentru soselele mai inguste, deci arata mult mai bine pe strazile 
  orasului. X3 arata aproape utilitar in comparatie cu el, deci poate fi perceput si ca o
  alternativa urbana pentru acest model, fara sa-si piarda nimic din caracterul dinamic.</p>
