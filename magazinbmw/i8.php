<h2>BMW i8 - supercarul viitorului</h2><br />
<div id="main">
 <img src="images/i8_title.jpg">
 <p> Practic cu i3 si i8, BMW lanseaza un nou brand, unul care va pune accentul 
 pe ecologie, dar fara a renunta la valorile marcii. Conceptul i8, de fapt un 
 preview al versiunii de serie, are la baza BMW Vision EfficientDynamic. 
 A conservat in proportie de aproape 100% designul avangardist al concept car-ului. 
 Este un plug-in hibrid, adica imprumuta sistemul de propulsie de la i3, dar de 
 aceasta data puterea este trimisa catre puntea fata. Iar pe puntea spate regasim 
 un propulsor cu trei cilindri, unul extrem de performant, care livreaza 220 CP 
 si 300 Nm. Este vorba despre un agregat turbo de 1,5 litri care a fost dezvoltat 
 de BMW, alaturi de propulsorul de 600 cmc in doi cilindri care va putea fi comanda 
 optional pentru marirea autonomiei in cazul lui i3. Combinat, rezulta o putere 
 de 348 CP si un cuplu motor de 550 Nm.</p>
<p>Din punct de vedere al performantelor BMW i8 pare a fi primul hibrid cu adevarat 
sportiv si asta pentru ca 0-100 km/h dureaza 4,6 secunde in timp ce viteza maxima este 
de 250 km/h. O repriza de la 80 km/h la 120 km/h va dura aproximativ 4 secunde. Si 
toate acestea pentru un consum mediu declarat de 2,7 litri. Sincer, nu am fi deranjati 
nici de un consum dublu la ce performante ofera i8, fara a mai lua in calcul designul.
 Bavarezii au reusit sa obtina o masa de doar 1.480 kg, ceea ce este bine pentru un plug-in hibrid.</p>
<p>Cele doua propulsoare vor functiona in tandem pentru o eficienta maxima si un consum 
cat mai redus. i8 va oferi si posibilitatea rularii in mod electric, in acest caz autonomia
 fiind limitata la aproximativ 35 km. Durata de incarcare abateriilor este de doar 105 minute, 
 ceea ce reprezinta un avantaj urias. Ai sansa de a putea incarca complet bateriile si in 
 cazul unei sesiuni de cumparaturi la un supermarket, sau cat timp esti la cinematograf, ori
 la o terasa. Singura conditie fiind sa traiesti intr-o tara care ofera aceasta posibilitate prin infrastructura. </p>

