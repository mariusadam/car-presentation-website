<h2>BMW i3</h2><br />
<div id="main">
 <img src="images/i3_title.jpg">
 <p>Reprezentantii BMW au anuntat ca au 100.000 de rezervari pentru testarea primului 
 sau vehicul electric, i3. Mai mult decat atat, Ian Robertson, seful departamentului 
 de vanzari de la BMW, sustine ca exista un numar semnificativ de clienti care au 
 depus un avans pentru primul model electric al marcii din Muchen. In ceea ce priveste
 pretul noului i3, Robertson spune cai modelul va fi apropiat de Seria 3 in aceasta 
 privinta. Versiunea de serie a lui BMW i3 va fi prezentata la Salonul Auto de la Frankfurt, 
 iar vanzarile vor incepe in ultima parte a anului curent. </p>
<p> BMW i3 este o investitie intr-un plan de viitor, iar din acest plan fac parte 
vehiculele cu zero emisii de CO2, care joaca un rol cheie in aceasta poveste. Suntem 
increzatori ca i3 si i8 vor schimba cererea pentru vehiculele de acest tip, pentru ca 
vom schimba tehnologia in sectorul vehiculelor electrice?, a declarat Ian Robertson in 
cadrul congresului Automotive News Europe. </p>
<p>Ian Robertson spune ca BMW i3 este diferit de oricare alt vehicul electric de pe
 piata prin conceptia sa, fiind dezvoltat din start sa devina un automobil electric, 
 in timp ce competitorii au vehicule conventionale carora li s-a adaptat un sistem de 
 propulsie electric. Lansarea lui BMW i3 vine la aproape 30 de luni de la anuntul 
 infiintarii diviziei "i" de la BMW, menita sa se ocupe doar de vehiculele electrice 
 si ecologice din gama producatorului din Munchen. Reprezentantul BMW se declara optimist 
 cu privire la dezvoltarile ulterioare ale acumulatorilor destinati vehiculelor electrice, 
 sustinand ca acestea vor evolua in urmatorii trei ani mai mult decat au evoluat in ultimul secol.</p>
