<h2>BMW 7 ActiveHybrid facelift</h2> <br />
<div id="main">
 <P><img src="images/n32_pret.jpg"></P>
 <p> <img src="images/n32_date.jpg" ></p>
 <p>&nbsp&nbsp&nbsp&nbsp Nu sunt multe lucruri noi de spus despre felul in care
 arata BMW Active Hybrid 7, pentru ca la exterior, privitorii il vor recunoaste 
 drept un 'septar' obisnuit, sau un Seria 7 facelift pentru cei cu ochiul format.
 Singura diferenta o fac emblemele Active Hybrid prezente pe montantii spate si 
 pe capota portbagajului. 
 <br />&nbsp&nbsp&nbsp&nbsp
 Aceleasi embleme sunt regasite si la nivel de habitaclu, pe interiorul pragurilor
 si pe trapa care acopera spatiul de depozitare de la baza consolei centrale. 
 In alta ordine de idei, luxul si comfortul sunt la ele acasa in cazul lui 
 Active Hybrid 7, cu nimic mai prejos decat ceea ce ofera de regula un Seria 7.
Habitaclul transmite acelasi sentiment placut de izolare, poate mai precis spus
 detasare, amplificat de materialele de inalta calitate, scaunele regesti din
 punct de vedere al confortului oferit si al pozitiei la volan (a nu se confunda 
 'regeste' cu  'scaunul mult dat pe spate, spatarul inclinat, mana stanga pe geam 
 si dreapta in varful volanului'). Totul este impecabil in centrul de comanda al
 limuzinei, incepand cu designul si terminand cu efectul vizual si tactil oferit de materiale.
<br />&nbsp&nbsp&nbsp&nbsp
 Singurul minus al acestei tehnologizari masive il reprezinta aglomeratia de 
 tab-uri, foldere si sub-foldere din interfata sistemului multimedia, insa dupa 
 cateva butonari repetate, navigarea prin meniuri devine o formalitate si nu mai
 pune probleme nici macar celor nefamiliarizati cu joystick-ul iDrive.
in plus, portbagajul a avut de suferit, pentru ca inginerii BMW au fost nevoiti 
sa dedice o buna parte din acesta bateriilor aferente sistemului Active Hybrid. 
In consecinta, volumul dedicat depozitarii bagajelor a scazut de la 500 de litri 
la 360 de litri.</p>
